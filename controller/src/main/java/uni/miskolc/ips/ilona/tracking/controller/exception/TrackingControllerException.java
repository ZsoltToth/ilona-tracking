package uni.miskolc.ips.ilona.tracking.controller.exception;

public class TrackingControllerException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public TrackingControllerException() {
		super();
	}

	public TrackingControllerException(String message) {
		super(message);
	}

	public TrackingControllerException(Throwable cause) {
		super(cause);
	}

	public TrackingControllerException(String message, Throwable cause) {
		super(message, cause);
	}

}